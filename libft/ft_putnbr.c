#include "libft.h"

void		ft_putnbr(int n)
{
	if (n >= INT_MAX)
	{
		ft_putstr("2147483647");
		return ;
	}
	if (n <= INT_MIN)
	{
		ft_putstr("-2147483648");
		return ;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
		ft_putchar(n + '0');
}
