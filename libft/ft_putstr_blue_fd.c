#include "libft.h"

void		ft_putstr_blue_fd(char const *str, int fd)
{
	ft_putstr_fd("\033[1;36m", fd);
	write(fd, str, ft_strlen(str));
	ft_putstr_fd("\033[0m", fd);
}
