#include "libft.h"

char		*ft_strcat(char *s1, const char *s2)
{
	char	*my_s1;

	my_s1 = s1;
	while (*my_s1)
		++my_s1;
	while (*s2)
	{
		*my_s1 = *s2;
		++my_s1;
		++s2;
	}
	*my_s1 = '\0';
	return (s1);
}
